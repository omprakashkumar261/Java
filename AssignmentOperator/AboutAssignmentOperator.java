package AssignmentOperator;

public class AboutAssignmentOperator {
    public static void main(String []args)
    {
        /*here ! used the Assignment Operator have been covered  */
        int marks= 86;
        System.out.println(marks);

        /*Another example of Assignment operator  i.e channing Assignment operator  */
        int m1, m2, m3, m4;
        m1=m2=m3=m4=87;
        System.out.println(m1+" "+m2+" "+m3+" "+m4);

        /*here !used the Assignment operator i.e compound Assignment operator  */
        int add =12; 
       // add =add+5;//simple adding the value ...
        add +=5; //this is the compound operator...
        System.out.println(add);
    }
}
